package Method_EX;

import java.util.Scanner;

public class M_EX_2 {
	public static void main(String[] args) {
		System.out.println("\nOutput is " + input());
	}
	
	static String input() {
		System.out.println("Enter a text!");
		try (Scanner input = new Scanner(System.in)) {
			String txt = input.next();
			return txt;
		}
	}
}
