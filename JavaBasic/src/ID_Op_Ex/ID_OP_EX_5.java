package ID_Op_Ex;

public class ID_OP_EX_5 {
	public static void main(String[] args) {
		int num1 = 78;
		int num2 = 45;
		int num3 = 62;
		double result = 0;
		
		result += num1;
		result += num2;
		result += num3;
		result /= 300;
		System.out.println("The average of total mark is " + result);
		
		result *= 100;
		System.out.println("The percentage of total mark is " + result + "%");
	}
}
